/**
shift()
the function is used to delete the first element of an array
array.shift()

unshift()
the function is used to add the new value in the starting of an array.
array.unshift("new value");
*/

var array = ["tesla","Kia","mazda","farrari"];
document.write(array + "<br>");

array.shift();
document.write(array + "<br>");

array.unshift("Dodge");
document.write(array + "<br>");
