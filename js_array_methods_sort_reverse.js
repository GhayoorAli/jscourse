/**
sort()
the function is used to sort the array elements alphabetically.
array.sort()

reverse()
the function is used to reverse the sorted the array elements.
array.reverse()
*/

var array = ["tesla","Kia","mazda","farrari"];
document.write(array + "<br>");
array.sort();
document.write(array + "<br>");
array.reverse();
document.write(array + "<br>");
